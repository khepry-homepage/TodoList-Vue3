import cookies from 'js-cookie'
/* 
  cName: String //cookie键
  cValue: String  //cookie值 
  cExpires: Number  //cookie过期时间(单位天)
  cDomain: String //cookie可视的域名
  isHttpOnly: Boolean //true表示禁止客户端js脚本读取cookie
 */
function set(cName, cValue, cExpires, cDomain, isHttpOnly) {
  try {
    // 将cookie过期时间作为值封装到json对象中，并且使用base64加密
    let jsonObj = {
      value: cValue,
      cExpires: new Date((new Date()).getTime() + cExpires * 86400000).getTime()
    }
    let encodeJsonStr = base64encode(JSON.stringify(jsonObj))
    cookies.set(cName, encodeJsonStr, {expires: cExpires, domain: cDomain, httpOnly: isHttpOnly})
    
  } catch (error) {
    console.log('cookie设置错误', error.message)
  }
}
function get(cName) {
  try {
    let decodeJsonObj = JSON.parse(base64decode(cookies.get(cName)))
    return decodeJsonObj
  } catch (error) {
    return undefined;
  }  
}

export default {
  set,
  get
}


